function createSideNavLink(item) {
    let elNavItem = document.createElement("li");
    elNavItem.className = "nav-item";
    // elNavItem.style = "background-color: " + item['colour']

    let elNavLink = document.createElement("a");
    elNavLink.href = item['url'];
    elNavLink.className = "nav-link py-3 border-bottom border-top text-white";
    elNavLink.title = item['tooltip'];
    elNavLink.setAttribute('aria-current', "page");
    elNavLink.setAttribute('data-bs-toggle', "tooltip");
    elNavLink.setAttribute('data-bs-placement', "right");
    elNavLink.setAttribute('data-bs-original-title',  item['tooltip']);
    
    elNavLink.innerHTML = item['svg'];

    elNavItem.appendChild(elNavLink);

    return elNavItem;
};

export function createSideNavItems() {
    for (let itemNo in itemsSideNav) {
        let elNavLink = createSideNavLink(itemsSideNav[itemNo]);
        containerSideNav.appendChild(elNavLink);
    }
}